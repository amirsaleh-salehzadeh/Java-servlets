package common;

/**
 *
 * @author Amirsaleh
 */
public class Chocolate {
private int price;
private String chocolateName;
private String chocolateDetail;
private String imageAddress;
private String brandName;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getChocolateName() {
        return chocolateName;
    }

    public void setChocolateName(String chocolateName) {
        this.chocolateName = chocolateName;
    }

    public String getChocolateDetail() {
        return chocolateDetail;
    }

    public void setChocolateDetail(String chocolateDetail) {
        this.chocolateDetail = chocolateDetail;
    }

    public String getImageAddress() {
        return imageAddress;
    }

    public void setImageAddress(String imageAddress) {
        this.imageAddress = imageAddress;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

}
